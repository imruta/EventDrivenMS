package com.order.service.order_service.pojo;

public class OrderInfo {
	public String OrderId;
	public String email;
	public String phoneNo;
	public Boolean paymentStatus=false;
	public Boolean orderStatus=false;
	public OrderInfo() {
		super();
	}
	public OrderInfo(String orderId, String email, String phoneNo, Boolean paymentStatus, Boolean orderStatus) {
		super();
		OrderId = orderId;
		this.email = email;
		this.phoneNo = phoneNo;
		this.paymentStatus = paymentStatus;
		this.orderStatus = orderStatus;
	}
	public String getOrderId() {
		return OrderId;
	}
	public void setOrderId(String orderId) {
		OrderId = orderId;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPhoneNo() {
		return phoneNo;
	}
	public void setPhoneNo(String phoneNo) {
		this.phoneNo = phoneNo;
	}
	public Boolean getPaymentStatus() {
		return paymentStatus;
	}
	public void setPaymentStatus(Boolean paymentStatus) {
		this.paymentStatus = paymentStatus;
	}
	public Boolean getOrderStatus() {
		return orderStatus;
	}
	public void setOrderStatus(Boolean orderStatus) {
		this.orderStatus = orderStatus;
	}
	@Override
	public String toString() {
		return "OrderInfo [OrderId=" + OrderId + ", email=" + email + ", phoneNo=" + phoneNo + ", paymentStatus="
				+ paymentStatus + ", orderStatus=" + orderStatus + "]";
	}
	

}
